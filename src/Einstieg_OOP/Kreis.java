package Einstieg_OOP;

public class Kreis {
    int positionX, positionY, radius;
    String farbe;

    Kreis() {

    }

    Kreis(int positionX, int positionY, int radius, String farbe) {
        this.positionX = positionX;
        this.positionY = positionY;
        this.radius = radius;
        this.farbe = farbe;
    }
}
